/*
	Contains all the COMPARISON OPERATORS: (USE ONLY SYMBOLS)
		--> NOT EQUAL:        <> , != ( CAN USE ANY ONE)
		--> GREATER THAN:     >
		--> LESSER THAN:      <
		--> GREATER OR EQUAL: >=
		--> lESSER OR EQUAL:  <=
		--> EQUAL :           =
		
*/


-- COMPARISON OPERATORS (>)
SELECT * 
FROM person
WHERE SCORE > 60

-- COMPARISON OPERATORS (>=)
SELECT * 
FROM person
WHERE SCORE >= 60

-- COMPARISON OPERATORS (<>)
SELECT * 
FROM person
WHERE SCORE <> 60


